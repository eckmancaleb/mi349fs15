    <h2>Design Paradigm</h2>
    <div class="block-text">
    	<p>I've always had an interest in information architecture.  For me, the way that people view and interact with information is almost as important as the information itself.  This interest has most naturally manifested in my Computer Science degree.  But more specifically, I've found that web design has been the most enjoyable thing I've learned in any of my classes.</p>
    	<p>The internet has become the new frontier for news, entertainment, education, and even socializing.  The experiences that we have with these web pages is becoming more and more important as we spend more time online.  In it's essence, the main purpose of the internet is to provide us with information, but I believe that it can be more than that.  Why shouldn't our interactions with this information be fun and enjoyable?  Couldn't these pages both inform and amaze?</p>
    	<p>Some of these projects aren't even information based.  The development of web technologies has allowed for complex motion graphics that were never possible five or ten years ago.  In some of these pages I made what I call "digital art;" a page that's simply fun or interesting to look at.  Emerging techologies allow for forms of expression that simply aren't possible in the physical world, and I think that's pretty exciting.</p>
