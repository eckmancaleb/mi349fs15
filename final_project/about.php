    <h2>Biography</h2>

    <div class="block-text">
    	<p>I was born as the first child of Jeff and Tori Eckman in Anderson, Indiana.  Due to the fact that my father is an ordained minister, my family has moved around a lot in the past twenty years.  During that time I've been blessed with four awesome younger siblings.  We've lived in Anderson, Indiana, and then spent some time in Defiance, Ohio.  Then, when I was in first grade we moved up to Michigan, where we've been ever since.</p>
    	<p>Most of my upbringing was spent in Midland, Michigan.  Specifically, I lived in an area called Lee township, but that was lovingly referred to as "Leetuckey." But despite the fact that I lived in a pretty rural area, I still believe that I got an excellent education at the Bullock Creek School District.  I appreciate the fact that it wasn't a huge school, because it gave me an opportunity to know almost everyone on a first name basis.  After I graduated in spring of 2012, I set my sights on Michigan State University as the college I would attend that fall.</p>
    	<p>The college experience taught me so much.  How to make new friends, how to study, and how to manage my time as an adult.  I've met so many people with so many different stories in my four years there, and will definitely miss that atmosphere when I graduate.  Although I began as a Mechanical Engineering major, I quickly gravitated towards the field of computer science.  I enjoy the problem solving aspect when approached with a programming problem.  I was also very interested in information architecture, and this is what has drawn me to web design, which I hope to be able to do for the rest of my life.</p>
    	<p>In the spring of 2013, my father began candidating at a new church in Battle Creek, Michigan.  This was kind of a shock to me because my family had lived in the same city for thirteen years.  This new job meant a new house, new city, and a new school for my siblings.  It was a tough transition for everyone because we left a lot of good people in Midland.  Even though I had already begun a new life at Michigan State, it was completely bizarre to come home to a different place than the one I grew up in.</p>
    	<p>The move was two years ago, and I'm happy to reporte that the city of Battle Creek has been wonderful.  Our new church family welcomed us with open arms, and has made life in a new place much easier.  Looking back, I'm glad that my family moved.  This place has provided wonderful opportunities for all of us, and I know there are still good things to come.</p>
